package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		var a = 4.0;
		var v = 1;
		var c = "teste inferencia";
		System.out.println("teste double | " + a + " | teste concluido");
		System.out.println("teste int | " + v + " | teste concluido");
		System.out.println("teste de texto | " + c + " | teste concluido com exito, inferencia correta.");
	}
}
